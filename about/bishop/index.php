            <p>The Rt. Rev. Nason Baluku was born on 15th March 1967 in Mahango Sub-County on the slopes of the Rwenzori Mountains in Kasese district. He holds a Master of Arts in Organizational Leadership and Management and a Bachelor of Community Leadership and Development, both from Uganda Christian University. He received a Diploma in Theology from Uganda Christian University and a Provincial Certificate in Theological Studies from Bishop Balya Theological College.</p>
            <p>He was born again at the age of 12 on 13th October 1979 and was confirmed three years later in 1982 by Bishop Kamanyire. By the time he was 17 years old, he was commissioned as a catechist, and later a lay reader. On 6th August 1995, he was ordained a deacon and made a priest on 28th July 1996. He was appointed as a canon of St. Paul’s Cathedral on 4th December 2005.</p>
            <p>The Bishop has served in a variety of ministry positions, including a vicar of several parishes, an archdeacon, diocesan secretary, program director for the Bishop Masereka Christian Foundation, PDR coordinator in the Provincial Secretariat, project manager for the international organization Barnabas Fund. He is married to Jane Biira Baluku and God has blessed them with seven children.</p>
