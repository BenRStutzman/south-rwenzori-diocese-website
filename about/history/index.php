      <h2>Origins of the Diocese</h2>
      <p>The creation of South Rwenzori Diocese is linked to the struggle for the Bamba-Bakonzo for
        self-administration. The yearning for an independent and separate district from Toro also crept into the
        church, which, like the local government, was headed by people who were not natives to the area. As
        parishes were created in the mountain areas, it became necessary for the church to have local people to
        help in spread the message of salvation. It is no wonder, therefore, that the first voices for a
        separate diocese came from Kitholhu parish, where pastoral work became difficult for a Kabarole–based
        bishop amidst the agitation for separate administration. Below is the chronology of events that led to
        the creation of South Rwenzori Diocese.</p>
